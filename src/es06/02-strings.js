// ES5 - concatenacion "normal"
let hello = 'hello';
let world = 'world';
let phrase1 = hello + ' ' + world + '!';
console.log(phrase1);

// ES6 - Template literales
let phrase2 = `${hello} ${world}!`;
console.log(phrase2);

